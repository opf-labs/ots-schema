package edu.harvard.hul.ois.ots.schemas.AES;

/** This interface applies to abstract schema types. In such cases, an
 *  abstract class represents the abstract type, and subclasses of it
 *  represent the concrete types which extend it. 
 */
public interface SchemaSubtype {

    /** Returns the type name for the class. Every concrete type class
     *  must have a type name, which is the value of the xsd:type
     *  attribute. */
    public String getTypeName ();

}
